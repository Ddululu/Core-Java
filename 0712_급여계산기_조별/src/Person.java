
public class Person {
	private int id;
	private int grade;
	private int ho;
	private int money;
	private int pay;
	private int tax;
	private int taxPay;
	public int getId() {
		return id;
	}
	public void setId(int id) {this.id = id;}
	public int getGrade() {return grade;}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	public int getHo() {
		return ho;
	}
	public void setHo(int ho) {
		this.ho = ho;
	}
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	public int getPay() {
		return pay;
	}
	public void setPay(int pay) {
		this.pay = pay;
	}
	public int getTax() {
		return tax;
	}
	public void setTax(int tax) {
		this.tax = tax;
	}
	public int getTaxPay() {
		return taxPay;
	}
	public void setTaxPay(int taxPay) {
		this.taxPay = taxPay;
	}
	
	
}
